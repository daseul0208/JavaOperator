package datatype;

public class StringDemo {
	public static void main(String[] args) {
		char c = 'c'; 
		// 맨 끝에 있는 ; 세미컬림을 구분자라고 한다
		// 카 테이터타입의 c 변수에 알파벳 c 값을 할당함
		System.out.println(c);
		String s = "abc";
		System.out.println(s);
		System.out.println("abc");
		// 알파벳 하나만 출력할 경우는 char 타입을 
		// 사용하고, 하나 이상 복수의 문자열을
		// 출력할 때는 String 타입을 사용한다
		// 주의할 점은 상클쿼터와 더블쿼터에 유의
	}
}
